print('6. FUA para ler dois inteiros (variáveis A e B) e efetuar as operações de adição, subtração, multiplicação e divisão de A por B apresentando ao final os quatro resultados obtidos. ')
a=int(input('Digite um numero:\n'))
b=int(input('Digite o segundo numero:\n'))
adicao= a+b
subtracao= a-b
multiplicacao= a*b
divisao= a/b
print(f'a adicao de {a} e {b} é = {adicao}\n')
print(f'a subtracao de {a} com {b} é = {subtracao}\n')
print(f'a multiplicacao de {a} vezes {b} é = {multiplicacao}\n')
print(f'o resultado da divisao de {a} e {b} é = {divisao}\n')